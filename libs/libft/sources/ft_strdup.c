#include "libft.h"

char	*ft_strdup(const char *src)
{
	char	*allocated_ptr;

	allocated_ptr = ft_strnew(ft_strlen(src));
	if (allocated_ptr == NULL)
		return (NULL);
	ft_strcpy(allocated_ptr, src);
	return (allocated_ptr);
}
